// C 练习实例13 - 水仙花数
// 题目：打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和等于该数 本身。例如：153是一个"水仙花数"，因为153=1的三次方＋5的三次方＋3的三次方。

#include <stdio.h>

int main() {
    for (int x = 1; x <= 9; x++) {
        for (int y = 0; y <= 9; y++) {
            for (int z = 0; z <= 9; z++) {
                if (x * 100 + y * 10 + z == x * x * x + y * y * y + z * z * z) {
                    printf("%d%d%d是一个水仙数\n", x, y, z);
                }
            }
        }
    }

    return 0;
}
